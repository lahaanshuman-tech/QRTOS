// ── VGA init ───────────────────────────────
void gfx_init() {
    fb = (unsigned char*)0xA0000;
}

// ── Core drawing ───────────────────────────
void gfx_pixel(int x, int y, unsigned char color) {
    if (x < 0 || x >= VGA_WIDTH || y < 0 || y >= VGA_HEIGHT) return;
    fb[y * VGA_WIDTH + x] = color;
}

void gfx_clear(unsigned char color) {
    int i;
    for (i = 0; i < VGA_WIDTH * VGA_HEIGHT; i++)
        fb[i] = color;
}

void gfx_rect_fill(int x, int y, int w, int h, unsigned char color) {
    int i, j;
    for (j = y; j < y+h; j++)
        for (i = x; i < x+w; i++)
            gfx_pixel(i, j, color);
}

void gfx_rect(int x, int y, int w, int h, unsigned char color) {
    int i;
    for (i = x; i < x+w; i++) {
        gfx_pixel(i, y, color);
        gfx_pixel(i, y+h-1, color);
    }
    for (i = y; i < y+h; i++) {
        gfx_pixel(x, i, color);
        gfx_pixel(x+w-1, i, color);
    }
}

// ── Text drawing ───────────────────────────
void gfx_char(int x, int y, char c, unsigned char fg, unsigned char bg) {
    int row, col;
    unsigned char idx = (unsigned char)c;
    if (idx >= 128) idx = '?';
    for (row = 0; row < FONT_H; row++) {
        unsigned char bits = font[idx][row];
        for (col = 0; col < FONT_W; col++) {
            if (bits & (0x01 << col))
                gfx_pixel(x+col, y+row, fg);
            else
                gfx_pixel(x+col, y+row, bg);
        }
    }
}

void gfx_str(int x, int y, const char* s, unsigned char fg, unsigned char bg) {
    int i;
    for (i = 0; s[i]; i++)
        gfx_char(x + i * FONT_W, y, s[i], fg, bg);
}

// ── CMOS time reader ───────────────────────
static unsigned char cmos_read(unsigned char reg) {
    unsigned char val;
    __asm__ volatile("outb %0, %1" : : "a"(reg), "Nd"((unsigned short)0x70));
    __asm__ volatile("inb %1, %0" : "=a"(val) : "Nd"((unsigned short)0x71));
    return val;
}

static unsigned char bcd2bin(unsigned char v) {
    return ((v >> 4) * 10) + (v & 0x0F);
}

static void pad2(unsigned char n, char* buf) {
    buf[0] = '0' + n/10;
    buf[1] = '0' + n%10;
    buf[2] = '\0';
}

// ── Terminal ───────────────────────────────
int term_cx = 0;
int term_cy = 0;
#define TERM_COLS (VGA_WIDTH  / FONT_W)
#define TERM_ROWS ((VGA_HEIGHT - TASKBAR_H) / FONT_H)
#define TERM_BG   COL_BLACK
#define TERM_X0   2
#define TERM_Y0   2

void term_scroll() {
    int row, col, i, j;
    for (row = 0; row < TERM_ROWS-1; row++) {
        int src_y = TERM_Y0 + (row+1)*FONT_H;
        int dst_y = TERM_Y0 + row*FONT_H;
        for (i = 0; i < FONT_H; i++)
            for (j = 0; j < VGA_WIDTH; j++)
                fb[(dst_y+i)*VGA_WIDTH+j] = fb[(src_y+i)*VGA_WIDTH+j];
    }
    gfx_rect_fill(0, TERM_Y0+(TERM_ROWS-1)*FONT_H, VGA_WIDTH, FONT_H, TERM_BG);
    term_cy = TERM_ROWS - 1;
}

void term_init() {
    term_cx = 0;
    term_cy = 0;
}

void term_putc(char c, unsigned char fg) {
    if (c == '\n') {
        term_cx = 0;
        term_cy++;
        if (term_cy >= TERM_ROWS) term_scroll();
        return;
    }
    if (c == '\b') {
        if (term_cx > 0) {
            term_cx--;
            gfx_char(TERM_X0 + term_cx*FONT_W,
                     TERM_Y0 + term_cy*FONT_H,
                     ' ', fg, TERM_BG);
        }
        return;
    }
    gfx_char(TERM_X0 + term_cx*FONT_W,
             TERM_Y0 + term_cy*FONT_H,
             c, fg, TERM_BG);
    term_cx++;
    if (term_cx >= TERM_COLS) {
        term_cx = 0;
        term_cy++;
        if (term_cy >= TERM_ROWS) term_scroll();
    }
}

void term_puts(const char* s, unsigned char fg) {
    int i;
    for (i = 0; s[i]; i++) term_putc(s[i], fg);
}

void term_putln(const char* s, unsigned char fg) {
    term_puts(s, fg);
    term_putc('\n', fg);
}

void term_clear() {
    gfx_rect_fill(0, 0, VGA_WIDTH, VGA_HEIGHT - TASKBAR_H, TERM_BG);
    term_cx = 0;
    term_cy = 0;
}

// ── Taskbar ────────────────────────────────
void ui_draw_taskbar() {
    int tb_y = VGA_HEIGHT - TASKBAR_H;

    // background - Win98 grey
    gfx_rect_fill(0, tb_y, VGA_WIDTH, TASKBAR_H, 7);

    // raised border top
    gfx_rect_fill(0, tb_y, VGA_WIDTH, 1, 15);     // bright top line
    gfx_rect_fill(0, tb_y+1, VGA_WIDTH, 1, 7);    // grey

    // Start button (Win98 style)
    gfx_rect_fill(2, tb_y+2, 36, TASKBAR_H-4, 7);
    gfx_rect_fill(2, tb_y+2, 36, 1, 15);          // top highlight
    gfx_rect_fill(2, tb_y+2, 1, TASKBAR_H-4, 15); // left highlight
    gfx_rect_fill(37, tb_y+2, 1, TASKBAR_H-4, 8); // right shadow
    gfx_rect_fill(2, tb_y+TASKBAR_H-3, 36, 1, 8); // bottom shadow
    gfx_str(5, tb_y+3, "Start", COL_BLACK, 7);

    // clock on right
    unsigned char h = bcd2bin(cmos_read(0x04));
    unsigned char m = bcd2bin(cmos_read(0x02));
    unsigned char s = bcd2bin(cmos_read(0x00));
    char th[3]; char tm[3]; char ts[3];
    pad2(h,th); pad2(m,tm); pad2(s,ts);

    // clock box
    int clk_x = VGA_WIDTH - 52;
    gfx_rect_fill(clk_x, tb_y+2, 50, TASKBAR_H-4, 7);
    gfx_rect_fill(clk_x, tb_y+2, 1, TASKBAR_H-4, 8);     // left shadow
    gfx_rect_fill(clk_x, tb_y+2, 50, 1, 8);               // top shadow
    gfx_rect_fill(clk_x+49, tb_y+2, 1, TASKBAR_H-4, 15); // right highlight
    gfx_rect_fill(clk_x, tb_y+TASKBAR_H-3, 50, 1, 15);   // bottom highlight

    // time text
    gfx_str(clk_x+3, tb_y+3, th, COL_BLACK, 7);
    gfx_char(clk_x+3+2*FONT_W, tb_y+3, ':', COL_BLACK, 7);
    gfx_str(clk_x+3+3*FONT_W, tb_y+3, tm, COL_BLACK, 7);
    gfx_char(clk_x+3+5*FONT_W, tb_y+3, ':', COL_BLACK, 7);
    gfx_str(clk_x+3+6*FONT_W, tb_y+3, ts, COL_BLACK, 7);
}

