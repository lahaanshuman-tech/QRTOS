// ── VGA mode switch ────────────────────────
void gfx_init() {
    fb = (unsigned char*)0xA0000;  // set at runtime!
    // NO int 0x10 here - bootloader already did it
}
// ── Core drawing ───────────────────────────
void gfx_pixel(int x, int y, unsigned char color) {
    if (x < 0 || x >= VGA_WIDTH || y < 0 || y >= VGA_HEIGHT) return;
    fb[y * VGA_WIDTH + x] = color;
}

void gfx_clear(unsigned char color) {
    int i;
    for (i = 0; i < VGA_WIDTH * VGA_HEIGHT; i++)
        fb[i] = color;
}

void gfx_rect_fill(int x, int y, int w, int h, unsigned char color) {
    int i, j;
    for (j = y; j < y+h; j++)
        for (i = x; i < x+w; i++)
            gfx_pixel(i, j, color);
}

void gfx_rect(int x, int y, int w, int h, unsigned char color) {
    int i;
    for (i = x; i < x+w; i++) {
        gfx_pixel(i, y, color);
        gfx_pixel(i, y+h-1, color);
    }
    for (i = y; i < y+h; i++) {
        gfx_pixel(x, i, color);
        gfx_pixel(x+w-1, i, color);
    }
}

// ── Text drawing ───────────────────────────
void gfx_char(int x, int y, char c, unsigned char fg, unsigned char bg) {
    int row, col;
    unsigned char idx = (unsigned char)c;
    for (row = 0; row < FONT_H; row++) {
        unsigned char bits = font[idx][row];
        for (col = 0; col < FONT_W; col++) {
            if (bits & (0x80 >> col))
                gfx_pixel(x+col, y+row, fg);
            else
                gfx_pixel(x+col, y+row, bg);
        }
    }
}

void gfx_str(int x, int y, const char* s, unsigned char fg, unsigned char bg) {
    int i;
    for (i = 0; s[i]; i++)
        gfx_char(x + i * FONT_W, y, s[i], fg, bg);
}

// ── Terminal ───────────────────────────────
int term_cx = 0;
int term_cy = 0;
#define TERM_COLS (VGA_WIDTH / FONT_W)   // 53
#define TERM_ROWS ((VGA_HEIGHT - 10) / FONT_H)  // 23 (leave taskbar)
#define TERM_BG   COL_BLACK
#define TERM_X0   0
#define TERM_Y0   0

void term_scroll() {
    int row, col;
    // scroll up by one row
    for (row = 0; row < TERM_ROWS-1; row++)
        for (col = 0; col < TERM_COLS; col++) {
            // copy char from row+1 to row
            int src_y = TERM_Y0 + (row+1)*FONT_H;
            int dst_y = TERM_Y0 + row*FONT_H;
            int src_x = TERM_X0 + col*FONT_W;
            int i;
            for (i = 0; i < FONT_H; i++) {
                int j;
                for (j = 0; j < FONT_W; j++)
                    fb[(dst_y+i)*VGA_WIDTH+(src_x+j)] = fb[(src_y+i)*VGA_WIDTH+(src_x+j)];
            }
        }
    // clear last row
    gfx_rect_fill(TERM_X0, TERM_Y0+(TERM_ROWS-1)*FONT_H, VGA_WIDTH, FONT_H, TERM_BG);
    term_cy = TERM_ROWS - 1;
}

void term_init() {
    term_cx = 0;
    term_cy = 0;
}

void term_putc(char c, unsigned char fg) {
    if (c == '\n') {
        term_cx = 0;
        term_cy++;
        if (term_cy >= TERM_ROWS) term_scroll();
        return;
    }
    if (c == '\b') {
        if (term_cx > 0) {
            term_cx--;
            gfx_char(TERM_X0 + term_cx*FONT_W, TERM_Y0 + term_cy*FONT_H, ' ', fg, TERM_BG);
        }
        return;
    }
    gfx_char(TERM_X0 + term_cx*FONT_W, TERM_Y0 + term_cy*FONT_H, c, fg, TERM_BG);
    term_cx++;
    if (term_cx >= TERM_COLS) {
        term_cx = 0;
        term_cy++;
        if (term_cy >= TERM_ROWS) term_scroll();
    }
}

void term_puts(const char* s, unsigned char fg) {
    int i;
    for (i = 0; s[i]; i++) term_putc(s[i], fg);
}

void term_putln(const char* s, unsigned char fg) {
    term_puts(s, fg);
    term_putc('\n', fg);
}

void term_clear() {
    gfx_rect_fill(0, 0, VGA_WIDTH, VGA_HEIGHT - 10, TERM_BG);
    term_cx = 0;
    term_cy = 0;
}

void term_prompt() {
    ui_draw_taskbar();
    term_puts("C:\\QRTOS> ", COL_LGREEN);
}

// ── UI ─────────────────────────────────────
void ui_draw_taskbar() {
    // taskbar at bottom
    gfx_rect_fill(0, VGA_HEIGHT-10, VGA_WIDTH, 10, COL_GREY);
    gfx_str(2, VGA_HEIGHT-9, "[QRTOS]", COL_BLACK, COL_GREY);
}

void ui_draw_desktop() {
    gfx_clear(COL_CYAN);  // teal desktop like Win98!
    ui_draw_taskbar();
}

void ui_draw_window(int x, int y, int w, int h, const char* title) {
    // window body
    gfx_rect_fill(x, y, w, h, COL_WHITE);
    // title bar
    gfx_rect_fill(x, y, w, 10, COL_BLUE);
    gfx_str(x+2, y+1, title, COL_WHITE, COL_BLUE);
    // border
    gfx_rect(x, y, w, h, COL_GREY);
}

